function do_comparison_operations() {
  console.log("Discussing about Comparison operators");

  var a = 5;
  var b = 10;
  var c = a == b; // Equals comparison (==)
  console.log(c);

  var d = a != b; // Not Equals comparison (!=)
  console.log(d);

  var e = a < b; // Less than (<)
  console.log(e);

  var f = a > b; // Greater than (>)
  console.log(f);

  var g = a <= b; // Less than or equal to (<=)
  console.log(g);

  var h = a >= b; // Greater than or equal to (>=)
  console.log(h);
}
